const renderExchangeRates = () => {
  const list = document.querySelector("#exchangeRateList");
  const updated = document.querySelector("#exchangeUpdated");
  if (!list || !updated) return;

  if (!exchangeRatesData?.rates?.length) {
    updated.textContent = t("exchangeLoading");
    return;
  }

  const localizedView = localizedExchangeViews[currentLang];
  const localizedRates = localizedView ? exchangeRatesData.crossRates?.[currentLang] : null;
  let sourceLabel = "TCMB";

  if (localizedView && localizedRates) {
    const rows = [
      { label: localizedView.usdLabel, value: localizedRates.usd },
      { label: localizedView.eurLabel, value: localizedRates.eur },
    ].filter((rate) => Number.isFinite(rate.value));

    if (rows.length) {
      list.innerHTML = rows
        .map((rate) => `<span>${rate.label} <strong>1 = ${formatCrossRate(rate.value)} ${localizedView.code}</strong></span>`)
        .join("");
      sourceLabel = localizedRates.source || sourceLabel;
    } else {
      list.innerHTML = exchangeRatesData.rates
        .map((rate) => `<span>${rate.label} <strong>${formatTryRate(rate.value)} TL</strong></span>`)
        .join("");
    }
  } else {
    list.innerHTML = exchangeRatesData.rates
      .map((rate) => `<span>${rate.label} <strong>${formatTryRate(rate.value)} TL</strong></span>`)
      .join("");
  }

  const checkedAt = new Date(exchangeRatesData.updatedAt || Date.now()).toLocaleString("tr-TR", {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
  const dataDate = exchangeRatesData.date ? ` · ${t("exchangeDataDate")}: ${exchangeRatesData.date}` : "";
  updated.textContent = `${t("exchangeChecked")}: ${checkedAt}${dataDate} - ${sourceLabel}`;
};

const loadExchangeRates = async () => {
  const updated = document.querySelector("#exchangeUpdated");
  if (!updated || window.location.protocol === "file:") return;
  try {
    const response = await fetch(`/api/exchange-rates?t=${Date.now()}`, { cache: "no-store" });
    if (!response.ok) throw new Error("Exchange API failed");
    exchangeRatesData = await response.json();
    renderExchangeRates();
  } catch (error) {
    updated.textContent = `${t("exchangeUnavailable")} · TCMB`;
  }
};

