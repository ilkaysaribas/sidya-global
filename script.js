const isIosDevice = () => /iphone|ipad|ipod/i.test(window.navigator.userAgent);

const isStandaloneApp = () =>
  window.matchMedia("(display-mode: standalone)").matches || window.navigator.standalone === true;

const showInstallPanel = (message) => {
  if (installPanelCopy) {
    installPanelCopy.textContent = message;
  }

  installPanel?.removeAttribute("hidden");
};

const hideInstallPanel = () => {
  installPanel?.setAttribute("hidden", "");
};

window.addEventListener("beforeinstallprompt", (event) => {
  event.preventDefault();
  deferredInstallPrompt = event;
  installButton?.classList.add("is-ready");
});

installButton?.addEventListener("click", async () => {
  if (isStandaloneApp()) {
    showInstallPanel("Sidya Global is already opening as an app window on this device.");
    return;
  }

  if (deferredInstallPrompt) {
    deferredInstallPrompt.prompt();
    const choice = await deferredInstallPrompt.userChoice;
    deferredInstallPrompt = null;
    installButton.classList.remove("is-ready");

    showInstallPanel(
      choice.outcome === "accepted"
        ? "App installation has started. Please confirm the install prompt on your device."
        : "App installation was cancelled. You can press the App button again to retry.",
    );
    return;
  }

  if (window.location.protocol === "file:") {
    showInstallPanel(
      "App installation cannot start from a local file. Open https://sidyaglobal.com on Android Chrome or iPhone Safari.",
    );
    return;
  }

  if (isIosDevice()) {
    showInstallPanel(
      "On iPhone, open this site in Safari, tap Share, then choose Add to Home Screen.",
    );
    return;
  }

  showInstallPanel(
    "If Android Chrome does not show the install prompt automatically, open the browser menu and choose Install app or Add to Home screen.",
  );
});

