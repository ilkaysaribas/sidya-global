const translatePage = () => {
  document.documentElement.lang = currentLang;
  document.querySelectorAll(".lang-option").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === currentLang);
  });

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (content[currentLang][key]) {
      node.textContent = content[currentLang][key];
    }
  });

  renderProducts();
  document.documentElement.classList.remove("is-loading");
};

const renderProducts = () => {
  const grid = document.querySelector("#productGrid");
  grid.innerHTML = products[currentLang]
    .map((product) => {
      const related = productPartners[product.id] || [];
      const relatedMarkup = related.length
        ? `
          <div class="related-companies">
            <strong>${content[currentLang].relatedCompanies}</strong>
            <div>
              ${related
                .map(
                  (company) => `
                    <span class="related-company">
                      <a class="site-action" href="${company.site}">
                        <img src="${company.logo}" alt="" aria-hidden="true" />
                        <span>${company.name}</span>
                      </a>
                      ${
                        company.catalog
                          ? `<a class="catalog-action" href="${company.catalog}">${content[currentLang].sampleCatalogCta}</a>`
                          : ""
                      }
                    </span>
                  `,
                )
                .join("")}
            </div>
          </div>
        `
        : "";

      return `
        <article class="product-card" id="${product.id}">
          <div>
            <span class="product-icon" aria-hidden="true">${product.icon}</span>
            <h3>${product.title}</h3>
            <p>${product.copy}</p>
          </div>
          <div class="product-meta">
            ${product.meta.map((item) => `<span>${item}</span>`).join("")}
          </div>
          ${relatedMarkup}
        </article>
      `;
    })
    .join("");
};

